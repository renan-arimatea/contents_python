"""

Dir e Help

Dir: Apresenta uma lista de funções e métodos que podem ser utilizados em algum tipo de dado.

Sintaxe: dir(valor / tipo de dado)

Help: Apresenta as intruções de utilização das funções e métodos disponíveis para determindado tipo de dado.

Sintaxe: help(função) ou help( valor / tipo de dado.método/função)

"""

# Dir
print(dir("urso"))
print(dir(18))
print(dir(float))

# Help
print(help(print))
print(help("Renan".title))
